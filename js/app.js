function geoDist(la1,lo1,la2,lo2){return Math.sqrt(Math.pow((la1-la2)*111000,2)+Math.pow((lo1-lo2)*111000*Math.cos(la1*Math.PI/180),2));}

// ── GLOBAL ROUTE PARAM HELPER ──
// Projects a lat/lng onto a route shape and returns the along-route distance (metres)
// from the shape start, and the perpendicular snap distance.
function routeParam(la,lo,shape){
  var cumDist=0,projDist=0,bestSnap=Infinity;
  for(var i=0;i<shape.length-1;i++){
    var ax=shape[i].la,ay=shape[i].lo,bx=shape[i+1].la,by=shape[i+1].lo;
    var midLat=(ax+bx)/2,cosLat=Math.cos(midLat*Math.PI/180);
    var dlat=bx-ax,dlng=(by-ay)*cosLat,len2=dlat*dlat+dlng*dlng;
    var segLen=geoDist(ax,ay,bx,by),tt,snap;
    if(len2<1e-12){tt=0;snap=geoDist(la,lo,ax,ay);}
    else{tt=Math.max(0,Math.min(1,((la-ax)*dlat+(lo-ay)*cosLat*dlng)/len2));
      snap=geoDist(la,lo,ax+tt*(bx-ax),ay+tt*(by-ay));}
    if(snap<bestSnap){bestSnap=snap;projDist=cumDist+tt*segLen;}
    cumDist+=segLen;
  }
  return{param:projDist,snap:bestSnap};
}

