// Extract lat/lng points from a shape between two along-route distances p1..p2
function shapeSlice(shape,p1,p2){
  if(!shape||shape.length<2)return[];
  // Ensure p1 < p2
  if(p1>p2){var tmp=p1;p1=p2;p2=tmp;}
  var pts=[],cumDist=0,started=false;
  for(var i=0;i<shape.length-1;i++){
    var ax=shape[i].la,ay=shape[i].lo,bx=shape[i+1].la,by=shape[i+1].lo;
    var segLen=geoDist(ax,ay,bx,by);
    var segEnd=cumDist+segLen;
    // Interpolate start point onto this segment
    if(!started&&segEnd>=p1){
      var t=(segLen>0)?(p1-cumDist)/segLen:0;
      pts.push([ax+t*(bx-ax),ay+t*(by-ay)]);
      started=true;
    }
    // Collect shape vertices that fall inside the window
    if(started&&segEnd<p2){
      pts.push([bx,by]);
    }
    // Interpolate end point onto this segment
    if(started&&cumDist<=p2&&segEnd>=p2){
      var t2=(segLen>0)?(p2-cumDist)/segLen:1;
      pts.push([ax+t2*(bx-ax),ay+t2*(by-ay)]);
      break;
    }
    cumDist=segEnd;
  }
  return pts;
}

// Draw (or redraw) the route-following blocked section for a disruption
function drawDisBlockedLine(dis){
  if(dis.blockedLine){map.removeLayer(dis.blockedLine);dis.blockedLine=null;}
  var shape=dis._primaryShape;
  var pts=[];
  if(shape&&dis.southXO&&dis.northXO){
    // Slice the shape between the two crossover parameters
    var p1=dis.southXO._param,p2=dis.northXO._param;
    pts=shapeSlice(shape,p1,p2);
  } else {
    // Fallback: straight line through available points
    if(dis.southXO)pts.push([dis.southXO.la,dis.southXO.lo]);
    pts.push([dis.la,dis.lo]);
    if(dis.northXO)pts.push([dis.northXO.la,dis.northXO.lo]);
  }
  if(pts.length>=2){
    dis.blockedLine=L.polyline(pts,{color:'#e040fb',weight:6,opacity:.6,dashArray:'10 5'}).addTo(map);
  }
}

